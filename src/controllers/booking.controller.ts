import { Request, Response } from "express";
import { BookingServices } from "../services";
export class BookingController {
    constructor () {

    }

    all = async (req: Request, res: Response) => {
        const { status, message, data } = await BookingServices.getAll();
        return res.status(status).json({
            message,
            data,
        });
    };

    one = async (req: Request, res: Response) => {
        const {id}=req.params
        const {status, message, data} = await BookingServices.getOne(Number(id));
        return res.status(status).json({
            message,
            data,
          });
    };
    create = async (req: Request, res: Response) => {
        const { status, message, data } = await BookingServices.create(req.body);
        return res.status(status).json({
          message,
          data,
        });
      };
      update = async (req: Request, res: Response) => {
        const {id}=req.params
        const { status, message, data } = await BookingServices.update(Number(id),req.body);
        return res.status(status).json({
          message,
          data,
        });
      };
    
      delete = async (req: Request, res: Response) => {
        const {id}=req.params
        const { status, message, data } = await BookingServices.delete(Number(id));
        return res.status(status).json({
          message,
          data,
        });
      };
}